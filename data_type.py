# DATA TYPE IN PYTHON 

# INTEGER
age = 20

# float 

height = 183.5

#string

name = "pratik singh (ps)"

# complex number 
complex_num = 3 + 4j

# printing 

print(age)
print(height)
print(name)
print(complex_num)

#checking data type 

print(type(age))
print(type(height))
print(type(name))
print(type(complex_num))


# LIST

fruits = ["apple", "banana", "mango"]

print(fruits)
print(type(fruits))


# TUPLE

colors = ("red", "green", "blue")

print(colors)
print(type(colors))


# DICTIONARY

student = {
    "name": "Rahul",
    "age": 20,
    "course": "Python"
}

print(student)
print(type(student))


# SET

numbers = {1, 2, 3, 4, 5}

print(numbers)
print(type(numbers))



